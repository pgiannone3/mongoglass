package upperPanel;

public class LimitDepth {

	private int limit;
	private int depth;
	
	public LimitDepth(int limit, int depth) {
		
		this.limit = limit;
		this.depth = depth;
		
	}
	
	public int getLimit() {
		return limit;
	}

	public int getDepth() {
		return depth;
	}
	
	
}
